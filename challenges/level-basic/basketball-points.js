// Você está contando pontos para um jogo de basquete, 
// dada a quantidade de 2 pontos marcados e 3 pontos marcados,
//  encontre os pontos finais para a equipe e devolva esse valor.

function somarPontos(dois, tres) {
    return (dois * 2) + (tres * 3);
}

